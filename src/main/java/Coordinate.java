public class Coordinate {
    private int x;
    private int y;
    
    //REQUIRES: xIn and yIn are posative cordinates
    public Coordinate(int xIn, int yIn) {
        x = xIn;
        y = yIn;
    }
    
    //EFFECTS: returns x value of cordinate
    public int getX() {
        return x;
    }
    
    //EFFECTS: returns y value of cordinate
    public int getY() {
        return x;
    }
}
